public class ForExample2 {
    public static void main(String[] args) {

        for(int i=1; i<=15; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        int j = 1;
        while(j <= 15) {
            System.out.print(j + " ");
            j++;
        }
    }
}
